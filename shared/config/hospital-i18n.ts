export function localizeHospitalResource(resource: HospitalResource, locale: HospitalLocale): HospitalResource {
  if (locale !== "en") return resource;
  return {
    ...resource,
    title: hospitalText(resource.title, locale),
    description: localizeDescription(resource.description, locale) ?? resource.description,
    columns: resource.columns.map((column) => ({ ...column, label: hospitalText(column.label, locale) })),
    fields: resource.fields.map((field) => localizeField(field, locale)),
  };
}

export function localizeHospitalModule(module: HospitalModule, locale: HospitalLocale): HospitalModule {
  if (locale !== "en") return module;
  return {
    ...module,
    title: hospitalText(module.title, locale),
    shortTitle: module.shortTitle ? hospitalText(module.shortTitle, locale) : module.shortTitle,
    description: hospitalText(module.description, locale),
    resources: module.resources.map((resource) => localizeHospitalResource(resource, locale)),
  };
}

function localizeField(field: HospitalField, locale: HospitalLocale): HospitalField {
  return {
    ...field,
    label: hospitalText(field.label, locale),
    placeholder: localizeDescription(field.placeholder, locale),
    options: field.options?.map((option) => ({ ...option, label: hospitalText(option.label, locale) })),
  };
}

function localizeDescription(value: string | undefined, locale: HospitalLocale) {
  if (!value) return value;
  if (locale !== "en") return value;
  if (value.startsWith("Connecté à ")) return `Connected to ${value.replace("Connecté à ", "")}`;
  if (value === "Rechercher par nom, code ou référence...") return "Search by name, code or reference...";
  if (value === "UUID") return "UUID";
  if (value === '{"key":"value"}') return value;
  return hospitalText(value, locale);
}
